<?php
session_start(); // Обязательно запуск сессии

// Проверяем, существует ли счётчик в сессии
if (!isset($_SESSION['counter'])) {
    // Первый визит — создаём счётчик
    $_SESSION['counter'] = 0;
    echo "Вы еще не обновляли страницу.";
} else {
    // Увеличиваем счётчик
    $_SESSION['counter']++;
    echo "Вы обновили страницу " . $_SESSION['counter'] . " раз(а).";
}
?>
